package ch10.web1;

public class 신동원 {
	private String 키;
	private String 몸무게;

	public 신동원(String 키, String 몸무게) {
		super();
		this.키 = 키;
		this.몸무게 = 몸무게;
	}

	public 신동원() {
	}

	public String get키() {
		return 키;
	}

	public void set키(String 키) {
		this.키 = 키;
	}

	public String get몸무게() {
		return 몸무게;
	}

	public void set몸무게(String 몸무게) {
		this.몸무게 = 몸무게;
	}
}
